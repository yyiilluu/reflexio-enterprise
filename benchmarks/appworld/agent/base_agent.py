"""
BaseAppWorldAgent — vanilla ReAct-style code-generation agent for AppWorld.

Iteratively generates Python code and executes it via world.execute() until
the task is completed or the step limit is reached.
"""

import logging
import re
import time
from dataclasses import dataclass, field

import litellm
from tenacity import retry, stop_after_attempt, wait_exponential

from benchmarks.appworld.agent.prompts import build_system_prompt

logger = logging.getLogger(__name__)

# Regex to extract Python code from markdown code blocks
CODE_BLOCK_PATTERN = re.compile(r"```(?:python)?\s*\n(.*?)```", re.DOTALL)

COMPLETION_MARKER = "complete_task"


@dataclass
class StepRecord:
    """
    Record of a single agent step (code generation + execution).

    Args:
        step (int): Step number (1-indexed)
        code (str): Python code generated by the LLM
        output (str): Execution output (stdout + stderr)
        error (bool): Whether the execution raised an error
        timestamp (float): Unix timestamp when step completed
    """

    step: int
    code: str
    output: str
    error: bool
    timestamp: float


@dataclass
class AgentTrace:
    """
    Complete execution trace of an agent solving a task.

    Args:
        task_id (str): AppWorld task identifier
        steps (list[StepRecord]): All execution steps
        completed (bool): Whether the agent called complete_task()
        total_steps (int): Number of steps taken
        elapsed_seconds (float): Total wall-clock time
        system_prompt (str): The system prompt used
    """

    task_id: str
    steps: list[StepRecord] = field(default_factory=list)
    completed: bool = False
    total_steps: int = 0
    elapsed_seconds: float = 0.0
    system_prompt: str = ""


class BaseAppWorldAgent:
    """
    Vanilla ReAct-style code-generation agent for AppWorld tasks.

    Generates Python code via LLM, executes it in the AppWorld environment,
    feeds output back, and repeats until task completion or step limit.

    Args:
        model (str): LLM model identifier (litellm-compatible)
        max_steps (int): Maximum number of code generation + execution steps
        temperature (float): LLM sampling temperature
    """

    def __init__(
        self,
        model: str = "minimax/MiniMax-M2.5",
        max_steps: int = 20,
        temperature: float = 0.0,
    ):
        self.model = model
        self.max_steps = max_steps
        self.temperature = temperature

    def _get_supervisor_info(self, world) -> str:
        """
        Extract supervisor persona information from the AppWorld task.

        Args:
            world: AppWorld instance with task metadata

        Returns:
            str: Formatted supervisor info string
        """
        task = world.task
        parts = []
        if hasattr(task, "supervisor") and task.supervisor:
            sup = task.supervisor
            if hasattr(sup, "first_name"):
                parts.append(f"Name: {sup.first_name} {getattr(sup, 'last_name', '')}")
            if hasattr(sup, "email"):
                parts.append(f"Email: {sup.email}")
        if not parts:
            parts.append("No supervisor information available.")
        return "\n".join(parts)

    def _get_api_docs(self, world) -> str:
        """
        Get compressed API documentation from AppWorld.

        Args:
            world: AppWorld instance

        Returns:
            str: Compressed API documentation string
        """
        try:
            return world.task.api_docs.compress_parameters()
        except Exception:
            logger.warning("Failed to compress API docs, using raw docs")
            return str(world.task.api_docs)

    def build_system_prompt(self, world) -> str:
        """
        Build the system prompt for this task.

        Subclasses can override this to inject additional context (e.g., Reflexio).

        Args:
            world: AppWorld instance with task and API docs

        Returns:
            str: Complete system prompt
        """
        return build_system_prompt(
            task_instruction=world.task.instruction,
            supervisor_info=self._get_supervisor_info(world),
            api_docs=self._get_api_docs(world),
        )

    def _extract_code(self, response_text: str) -> str:
        """
        Extract Python code from the LLM response.

        Looks for markdown code blocks first. Falls back to treating the
        entire response as code if no blocks are found.

        Args:
            response_text (str): Raw LLM response text

        Returns:
            str: Extracted Python code
        """
        matches = CODE_BLOCK_PATTERN.findall(response_text)
        if matches:
            return matches[0].strip()
        # Fallback: treat entire response as code
        return response_text.strip()

    @retry(
        stop=stop_after_attempt(3),
        wait=wait_exponential(multiplier=1, min=2, max=30),
        reraise=True,
    )
    def _generate(self, messages: list[dict]) -> str:
        """
        Call the LLM to generate the next code block.

        Uses tenacity for retry logic on transient API failures.

        Args:
            messages (list[dict]): Conversation messages in OpenAI format

        Returns:
            str: Raw LLM response content
        """
        response = litellm.completion(
            model=self.model,
            messages=messages,
            temperature=self.temperature,
        )
        return response.choices[0].message.content or ""

    def solve(self, world) -> AgentTrace:
        """
        Run the full agent loop to solve an AppWorld task.

        Iteratively generates code, executes it, and feeds output back
        until the agent calls complete_task() or the step limit is reached.

        Args:
            world: AppWorld instance (used as context manager externally)

        Returns:
            AgentTrace: Complete execution trace with all steps and metadata
        """
        start_time = time.time()
        system_prompt = self.build_system_prompt(world)
        messages = [{"role": "system", "content": system_prompt}]

        trace = AgentTrace(
            task_id=world.task.task_id,
            system_prompt=system_prompt,
        )

        for step_num in range(1, self.max_steps + 1):
            logger.info(
                "Task %s — step %d/%d", world.task.task_id, step_num, self.max_steps
            )

            # Generate code
            try:
                raw_response = self._generate(messages)
            except Exception as e:
                logger.error("LLM generation failed at step %d: %s", step_num, e)
                error_output = f"LLM ERROR: {e}"
                trace.steps.append(
                    StepRecord(
                        step=step_num,
                        code="",
                        output=error_output,
                        error=True,
                        timestamp=time.time(),
                    )
                )
                messages.append({"role": "user", "content": error_output})
                continue

            code = self._extract_code(raw_response)
            messages.append({"role": "assistant", "content": raw_response})

            # Execute code in AppWorld
            try:
                output = world.execute(code)
                is_error = False
            except Exception as e:
                output = f"EXECUTION ERROR: {e}"
                is_error = True

            step_record = StepRecord(
                step=step_num,
                code=code,
                output=str(output),
                error=is_error,
                timestamp=time.time(),
            )
            trace.steps.append(step_record)

            # Feed output back to the model
            messages.append({"role": "user", "content": str(output)})

            # Check if agent called complete_task
            if COMPLETION_MARKER in code:
                trace.completed = True
                logger.info(
                    "Task %s completed at step %d", world.task.task_id, step_num
                )
                break

        trace.total_steps = len(trace.steps)
        trace.elapsed_seconds = time.time() - start_time
        return trace
